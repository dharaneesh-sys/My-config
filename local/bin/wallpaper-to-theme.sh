#!/bin/bash
# Usage: wallpaper-to-theme.sh <wallpaper-path>
# Generates a "Dynamic" palette from wallpaper using Matugen,
# then calls theme-switcher to propagate to all apps.

set -euo pipefail

WALLPAPER="${1:-}"
if [[ -z "$WALLPAPER" ]]; then
    WALLPAPER=$(cat ~/.cache/wallpaper/last_wallpaper 2>/dev/null || echo "")
fi
if [[ -z "$WALLPAPER" || ! -f "$WALLPAPER" ]]; then
    notify-send "Matugen" "No wallpaper found at $WALLPAPER"
    exit 1
fi

DYNAMIC_DIR="$HOME/.config/hypr/themes/Dynamic"
mkdir -p "$DYNAMIC_DIR"
PALETTE="$DYNAMIC_DIR/palette"

# Generate the entire palette via a single Python script that reads matugen JSON from stdin
matugen image "$WALLPAPER" --json hex --prefer value 2>/dev/null | python3 -c "
import json, sys

data = json.load(sys.stdin)
colors = data.get('colors', {})

def get_color(name, fallback):
    \"\"\"Extract color hex from matugen JSON, falling back to given default.\"\"\"
    entry = colors.get(name)
    if entry and isinstance(entry, dict):
        # Use default variant (same as dark in dark mode)
        variant = entry.get('default') or entry.get('dark') or {}
        return variant.get('color', fallback)
    return fallback

header = '# M3 Color Palette: Dynamic (auto-generated from wallpaper)\n# Generated by wallpaper-to-theme.sh'
print(header)

# Core tones with Everforest fallbacks
core_tones = {
    'primary': '#a7c080', 'on_primary': '#2d353b', 'primary_container': '#3d484d',
    'on_primary_container': '#a7c080', 'secondary': '#d3c6aa', 'on_secondary': '#2d353b',
    'tertiary': '#7fbbb3', 'surface': '#2d353b', 'on_surface': '#d3c6aa',
    'error': '#e67e80', 'outline': '#5f6c71',
    'on_tertiary': '#2d353b', 'on_error': '#2d353b',
    'surface_variant': '#3d484d',
}

for name, fallback in core_tones.items():
    print(f'{name}={get_color(name, fallback)}')

# Extended tones (all remaining M3 palette entries)
extended = [
    'primary_fixed', 'primary_fixed_dim', 'on_primary_fixed', 'on_primary_fixed_variant',
    'secondary_fixed', 'secondary_fixed_dim', 'on_secondary_fixed', 'on_secondary_fixed_variant',
    'tertiary_fixed', 'tertiary_fixed_dim', 'on_tertiary_fixed', 'on_tertiary_fixed_variant',
    'secondary_container', 'on_secondary_container',
    'tertiary_container', 'on_tertiary_container',
    'error_container', 'on_error_container',
    'surface_dim', 'surface_bright', 'surface_container_lowest',
    'surface_container_low', 'surface_container', 'surface_container_high', 'surface_container_highest',
    'on_surface_variant', 'inverse_surface', 'inverse_on_surface',
    'inverse_primary', 'outline_variant', 'scrim', 'shadow', 'source_color',
]

fallback_black = '#000000'
for tone in extended:
    print(f'{tone}={get_color(tone, fallback_black)}')
" > "$PALETTE"

# Call theme-switcher to propagate to all apps
~/.local/bin/theme-switcher Dynamic

notify-send "Matugen" "Theme generated from wallpaper colors"
